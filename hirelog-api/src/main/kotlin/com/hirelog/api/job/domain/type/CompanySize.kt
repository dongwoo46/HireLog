package com.hirelog.api.job.domain.type

/**
 * 회사 규모 (스타트업 단계 세분화)
 *
 * LLM이 JD 문맥 + 브랜드 인지도로 추정하는 값.
 * 스타트업 구인이 많은 한국 IT 채용 시장 특성에 맞게 세분화.
 *
 * 설계 원칙:
 * - 투자 단계(Series) 기준으로 스타트업을 3단계 분리
 * - 토스/당근/컬리처럼 유니콘급 스케일업은 대기업과 명확히 구분
 * - 외국계는 국내 규모와 무관하게 별도 분류
 */
enum class CompanySize(val label: String) {

    /**
     * 시드 단계
     * Pre-Seed / Seed 투자, ~10명, 아직 PMF 탐색 중
     */
    SEED("시드 스타트업"),

    /**
     * 초기 스타트업
     * Series A, 10~50명, PMF 확인 후 초기 성장
     */
    EARLY_STARTUP("초기 스타트업"),

    /**
     * 성장 스타트업
     * Series B~C, 50~300명, 빠른 스케일링 단계
     */
    GROWTH_STARTUP("성장 스타트업"),

    /**
     * 스케일업 (유니콘/예비유니콘)
     * Series C+ / 기업가치 1조+ 또는 그에 준하는 규모
     * 예: 토스, 당근, 컬리, 야놀자, 마켓컬리
     * 300명 이상이지만 상장 대기업이 아닌 경우
     */
    SCALE_UP("스케일업"),

    /**
     * 중소/중견기업
     * 상장 중소기업 또는 스타트업 계보가 아닌 전통 IT 기업
     * 예: 더존비즈온, 웹케시, 이노션 등
     */
    MID_SIZED("중소/중견기업"),

    /**
     * 대기업
     * 대규모 그룹사 또는 코스피 상장 대형사
     * 예: 카카오, 네이버, 삼성SDS, LG CNS, 현대오토에버, KT, SKT
     */
    LARGE_CORP("대기업"),

    /**
     * 외국계
     * 글로벌 본사를 둔 기업의 한국 법인 또는 지사
     * 예: Google Korea, Amazon, Microsoft, Oracle, SAP
     */
    FOREIGN_CORP("외국계"),

    /**
     * 판단 불가
     * JD에서 규모를 추정할 단서가 없는 경우
     */
    UNKNOWN("알 수 없음")
}